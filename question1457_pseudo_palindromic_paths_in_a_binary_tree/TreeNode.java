package question1457_pseudo_palindromic_paths_in_a_binary_tree;

public class TreeNode {

  int val;

  TreeNode left;

  TreeNode right;

  TreeNode() {
  }

  TreeNode(int val) {
    this.val = val;
  }

  TreeNode(int val, TreeNode left, TreeNode right) {
    this.val = val;
    this.left = left;
    this.right = right;
  }

}