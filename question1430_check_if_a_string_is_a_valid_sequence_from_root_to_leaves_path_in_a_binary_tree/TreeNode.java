package question1430_check_if_a_string_is_a_valid_sequence_from_root_to_leaves_path_in_a_binary_tree;

public class TreeNode {

  int val;

  TreeNode left;

  TreeNode right;

  TreeNode() {
  }

  TreeNode(int val) {
    this.val = val;
  }

  TreeNode(int val, TreeNode left, TreeNode right) {
    this.val = val;
    this.left = left;
    this.right = right;
  }

}